<p>In 2009 we lost  our local newspapers making it difficult to know what’s happening around the community. The Port Talbot Magnet was started online to provide local news and information and this is your chance to start taking control of the news that matters to you with a new platform called Little J.</p>

<p>Little J gives you the tools to become a local reporter, photographer or reviewer and work alongside the Magnet journalists to create stories you care about. </p>

<p>You can submit reports, stories and photos to this new community newspaper by using Little J. You can join in through the website, on Facebook, through a new smartphone app or even just text your contributions.  Little J is brand new and currently being tested and really needs your feedback to help shape it into something that you’ll find simple and easy to use.</p>

<h2>How do you use Little J?</h2>

<p>Well, you’re here so you’ve already created your account, so how you create a report?</p>

<ul>
<li>You can submit reports through the big pink ‘Add a report’ button on the dashboard</li>
<li>Try and give as much detail as you have in your report, this will help the local journalists</li>
<li>Remember to type in the location of the report, this could be where you are now or where your report refers to (e.g. Vivian Park)</li>
<li>If you have a photo you can upload it</li>
<li>Save your report</li>
</ul>

<h2> What happens to my report then?</h2>

<p>Once you submit your report it will appear for the local journalists to review. They may need to ask for more details and will get back to you if so.</p>

<p>If your report is used in a published story then you will be credited for your input.  With an account you have a record of all your activity and will start to build-up your reputation alongside other Little J’s.</p>

<p>You can also give reports in other places too.</p>

<p><b>On Facebook:</b> Search for ‘Littlej’ on Facebook and use the app to make a report.</p>

<p><b>On your smartphone</b>: If you have an Android smartphone or tablet you can download the FREE Little J app from the Google Play Store and use it to create reports, take photos and locate your report on a map. Simply open the Google Play Store on your device and search for ‘Little J’.</p>

<p><b>On Twitter:</b> Include the hashtag #littlej within your tweet and Little J will see it and put it into the main system. It would really help if you also geo-tag your tweet to locate your report.</p>

<p><b>By email</b>: You can email reports and photos to <a href="mailto:reports@littlej.org?Subject=Little%20J%20Report" style="color:#000;">reports@littlej.org</a></p>

<p><b>By SMS:</b> Send your report by text message to 077583 532589 and please include your name in the text (messages will be charged at your normal rate we’re afraid).</p>

<h2>Some questions you may want answers to.</h2>

<p>Is Little J fully ready?</b>
<p>Oh no, far from it to be honest, but it needs your feedback on the early ideas to make it something that you would want to regularly use.</p>

<p>What do I get out of it?</p>
<p>This is a community project and we want to proudly shout about people who take part so all reports, photos or ideas used will be openly credited to you in the paper. They will also appear on the Port Talbot Magnet website, credited to you. If you sign-up for a Little J account then you will see a record of all the reports you send in and will be able to increase your reputation as a citizen reporter. Little J is built so that the more you take part, the more your reputation builds in the community and the more you are able to do, such as take part in special assignments, vote on other people’s reports and even become a full editor with the Magnet.</p>

<p>Will I get any money?</p>
<p>No. This is a free tool to enable you to get a better news service in Port Talbot and whilst you can build up your reputation as a member of the Little J community, no reimbursement for contributions are available.</p>

<p>Where does Little J come from?</p>
<p>Little J is a research and development project funded through REACT and the Arts and Humanities Research Council investigating the future of print. The project is jointly run by Cardiff University Journalism Department, the Centre for Community Journalism and creative agency Behaviour.</p>

<p>What’s the future of Little J?</p>
<p>After the testing day on Saturday 13th April Little J will be tweaked and improved from the feedback you give. If successful Little J will then be launched fully in Port Talbot later this year and will then be made available to all other hyperlocal news projects in Wales.</p>


